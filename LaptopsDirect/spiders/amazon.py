    def parse_item(self, response):
        l = ItemLoader(item=LaptopsdirectItem(), response=response)

        #Scrape Fields
        l.add_xpath('title', '//*[@id="Head"]/title/text()')
        l.add_xpath('sku', '//*[@id="prodDetails"]//tbody//tr//td//a[starts-with(.," Model ")]/following::td[1]/text()')
        l.add_xpath('price', '//*[@id="priceblock_ourprice"]/text()')
        l.add_xpath('product_title', '//*[@id="productTitle"]/text()')
        l.add_xpath('stock', '//*[@id="availability"]/span/text()')

        # Administration Fields
        l.add_value('url', response.url)
        l.add_value('project', self.settings.get('BOT_NAME'))
        l.add_value('useragent', self.settings.get('USER_AGENT'))
        l.add_value('spider', self.name)
        l.add_value('server', socket.gethostname())
        l.add_value('date', datetime.now().strftime('%Y-%m-%d %H:%M:%S'))

        return l.load_item()